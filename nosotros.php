		<h3>ALGO DE HISTORIA</h3>
		
<p>La Asociación de Ambientalistas del Oriente AVANSO nació en el 2003 como organización subregional (Valles de San Nicolás) y en el 2015 se transformó en una organización regional (de las 5 zonas del área de influencia de Cornare) que aglutina organizaciones y procesos socio ambientales del oriente antioqueño.</p>

<p>Ya desde años atrás, como organización ambiental que aglutina y representa a otras organizaciones del Oriente Antioqueño se ha venido respondiendo al proceso de articulación estratégica para la implementación de la red departamental de mesas ambientales, a la cual nos vinculamos desde sus inicios dando respuesta a una apuesta que se viene legitimando desde la Ordenanza # 058 del 10 de Diciembre del 2014 para lograr afianzar el ejercicio ciudadano desde lo ambiental, desde lo local hasta cada una de las subregiones y el departamento, lo cual ha implicado entre otras acciones, los espacios internos de discusión  de AVANSO y en la red departamental de mesas ambientales (formulación, participación en audiencias públicas en la asamblea departamental, visitas a otras subregiones del departamento, reuniones con la Secretaría del medio ambiente de la gobernación y con la autoridad ambiental de la subregión Cornare) entre otras actividades desarrolladas. Igualmente, pensarse y actuar en red nos ha implicado el análisis de su estructura, mecanismos, representatividad, pero sobre todo, hacernos la pregunta de ¿si tiene o no validez una organización regional?</p>
<p>Como organización regional AVANSO ha realizado varios encuentros anuales de ONGs ambientalistas, los cuales han tenido énfasis temáticos como estrategia de formación.</p>

<p>Su mayor esfuerzo ha estado dedicado a tejer redes y a dinamizar los procesos organizativos desde la base (municipios y temáticas específicas), desarrollando su propuesta de Escuela de Líderes en la medida en que se ha contado con recursos para ello.</p>

<p>En lo organizacional, AVANSO asumió con recursos propios, un ejercicio participativo de planeación estratégica entre los años 2016 y 2017, dando como resultado una mayor claridad de las apuestas y estrategias de actuación en la región.</p> 

<p>AVANSO tiene representación en:</p>

<p>•	La junta directiva de la Corporación Masbosques.</p>

<p>•	Grupo de organizaciones sociales y comités de seguimiento a los acuerdos suscritos en el marco de los “Diálogos por la custodia del agua”, con especial énfasis en la mesa temática de Conservación del patrimonio natural.
</p>
<p>•	La Red departamental de mesas ambientales.</p> 

<p>•	Sistema Regional de Áreas Protegidas SIRAP Embalses.</p>

<p>•	CODEAM (Consejo departamental ambiental).</p>


<p>Ha participado activamente en:</p>

<p>•	Espacios puntuales convocados por terceros: Procesos regionales como el  Encuentro de ONGs de Cornare y el encuentro de Mesas ambientales, así como procesos nacionales como la iniciativa Convergencia, hacia la creación de una política transparente.</p>

<p>•	Relacionamiento y gestión con EPM y el Centro de Estudios Interculturales de la Universidad Javeriana de Cali de un proyecto regional de construcción de paz territorial alrededor del tema de la generación de energía como potencial generador de conflictos, el cual finalmente no se pudo concretar.</p>

<p>•	Formulación de propuesta técnica como proponentes del Plan de desarrollo turístico de San Luis.</p>

<p>•	Anfitriones-organizadores de Bioconstruyendo Colombia 2017 en San Rafael, junto con varios asociados (Red local de turismo y Fundación Darién).</p>

<p></p>
<p>•	Escuela de lieres y lideresas ambientales 2018-2019, convenio con la Corporación Prodepaz, lo cual permitió  un proceso de formación para el fortalecimiento de la red.</p>

<p>•	Gestiòn del proyecto En Red Ando convenio prodepaz, destinado al fortalecimiento de las redes ambientales  municipales.</p>

 
<p>•	Gestión de cofinanciación con Cornare, a lo cual ya se dio una respuesta positiva y se espera formalizar.</p>

<p>En los últimos años ha suscrito varios convenios y contratos entre los cuales cabe resaltar los siguientes:</p>

<p>•	Convenio de cooperación con Cornare entre 2015 y 2016 por un valor de $69.628.750</p>
<p>•	Convenio marco de cooperación con la Corporación Prodepaz en 2018 y la ejecución de varios encuentros de formación en el marco de dicho convenio.</p>

<p>AVANSO apuesta a generar, con la participación de todos, un desarrollo sostenible y a escala humana, en un territorio con alta biodiversidad y una oferta importante de bienes y servicios ecosistémicos, donde confluyen diversos intereses, generando en muchos casos conflictos por los cambios acelerados en lo ambiental, social, cultural y económico que vienen ocurriendo y sobre todo, porque la participación ciudadana se ha convertido en un eslogan para llenar planillas de asistencia y validar las intervenciones institucionales y empresariales,.</p>
<p>AVANSO es a la vez necesidad y oportunidad de reconstruir una verdadera participación del movimiento ambiental en una región que ha sido ejemplo de movilización social en defensa de los derechos colectivos y del ambiente, qué por el conflicto armado quedó sin líderes, con desconfianzas y sin poderse recuperar, en un territorio que le han llegado al momento toda una serie de avalanchas de proyectos de intervención que prometen “El desarrollo”.</p>


