		<h3>PORQUE UNA ORGANIZACIÓN REGIONAL</h3>
		<p>Estamos frente a un panorama de crisis ambiental y de humanidad que nos exige respuestas ai queremos que nuestros hijos continúen teniendo un cambio azul con agua cristalina y tierras fértiles, y nos honrren por lo debiamos haber hecho y podríamos haber hecho por defender la vida e todas sus formas, por impulsar propuestas y alternativas en la forma en que nos relacionamos entre nosotros y con la naturaleza (tecnologías adecuadas, sistemas productivos, comercialización de productos, redes de emprendimiento entre otros), y porque decidimos avanzar para decir desde el corazón que somos parte de la red de la vida y no sus dueños.</p>
		
